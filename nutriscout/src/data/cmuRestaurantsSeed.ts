const CMU_LAT = 40.4444;
const CMU_LNG = -79.9428;

function calcDistance(lat: number, lng: number): number {
  const R = 3959;
  const dLat = (lat - CMU_LAT) * Math.PI / 180;
  const dLng = (lng - CMU_LNG) * Math.PI / 180;
  const a = Math.sin(dLat/2)**2 + Math.cos(CMU_LAT*Math.PI/180)*Math.cos(lat*Math.PI/180)*Math.sin(dLng/2)**2;
  return R * 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1-a));
}

